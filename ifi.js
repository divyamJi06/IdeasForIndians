function filltable() {
    var table = document.getElementById("html-data-table");

    for (var i = 0; i < data.length; i++) {
        var row = table.insertRow(-1);

        var cell1 = row.insertCell(0);
        cell1.innerHTML = data[i].id;

        var cell2 = row.insertCell(1);
        // Create a clickable link for the title
        var titleLink = document.createElement("a");
        titleLink.href = "details.html?id=" + data[i].id; // Link to a details page with query parameter
        titleLink.innerHTML = data[i].title;
        cell2.appendChild(titleLink);

        var cell3 = row.insertCell(2);
        cell3.innerHTML = data[i].user.name;

        var cell4 = row.insertCell(3);
        cell4.innerHTML = data[i].likes;
    }
}

